const DICTS: Record<Locale, Dict> = { en, th };

export function t(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>,
): string {
  let s = DICTS[locale]?.[key] ?? DICTS[DEFAULT_LOCALE][key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(`{${k}}`, String(v));
    }
  }
  return s;
}
